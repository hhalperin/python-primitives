# Examples and use cases for Time and Space Complexity

# --- Examples ---

# O(1) example
my_list = [1, 2, 3]
print(my_list[0])  # Access by index is O(1)

# O(n) example
for x in my_list:
    print(x)  # Looping through a list is O(n)

