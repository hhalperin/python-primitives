# Examples and use cases for Basic Operations

# --- Examples ---

# Arithmetic
print(8 + 2)    # 10
print(8 - 2)    # 6
print(8 * 2)    # 16
print(8 / 2)    # 4.0
print(8 // 3)   # 2
print(8 % 3)    # 2
print(2 ** 4)   # 16

# Comparison
print(5 > 3)    # True
print(5 == 3)   # False

# Logical
print(True and False)  # False
print(True or False)   # True
print(not False)       # True

# Assignment
x = 10
x -= 3
print(x)  # 7

