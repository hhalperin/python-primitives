# Examples and use cases for Data Types

# --- Examples ---

# Integer
a = 10
print(a, type(a))  # 10 <class 'int'>

# Float
b = 3.1415
print(b, type(b))  # 3.1415 <class 'float'>

# Boolean
c = False
print(c, type(c))  # False <class 'bool'>

# String
d = "Python"
print(d, type(d))  # Python <class 'str'>

